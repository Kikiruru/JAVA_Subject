package Week04;

public class SignOperatorExample {
	public static void main(String[] args) {
		int x = -100;
		int result1 = +x; // 빈칸
		int result2 = -x; // 빈칸
		System.out.println("result1="+ result1);
		System.out.println("result2="+ result2);
		short s = 100; 
		int result3 = -s; // 타입 빈칸
		System.out.println("result3="+ result3);
	}
}
