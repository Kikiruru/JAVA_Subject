package Week09;

public class ex16_cal {
	void powerOn() {
		System.out.println("전원을 켭니다.");
	}
	
	int divide(int x, int y) {
		int result = x + y;
		return result;
	}
	
	double plus(int x, int y) {
		double result = (double) x / (double) y; // E, F
		return result;
	}
	
	void powerOff() {
		System.out.println("전원을 끕니다");
	}
}
